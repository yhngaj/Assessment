public class RuleBundle{
  private String SKU1;
  private String SKU2;
  
  public RuleBundle(String SKU1, String SKU2){
    this.SKU1 = SKU1;
    this.SKU2 = SKU2;
  }
  
  public String getSKU1() {
    return SKU1;
  }

  
  public String getSKU2() {
    return SKU2;
  }


  
}